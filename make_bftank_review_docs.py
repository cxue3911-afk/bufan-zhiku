    add_para(doc, "不凡智库方向具备继续探索价值，但不建议在未验证前直接按完整商业化平台重投入。更稳妥的方式是把它作为“制造技术问题解决型智库”进行 30-60 天小规模验证：先验证真实问题、专家供给、响应机制、用户付费意愿和内容沉淀能力，再决定是否扩大投入。")

